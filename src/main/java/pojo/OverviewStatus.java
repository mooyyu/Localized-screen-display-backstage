package pojo;

public class OverviewStatus {
    private int panelCode;
    private String moduleCodeList;

    public int getPanelCode() {
        return panelCode;
    }

    public void setPanelCode(int panelCode) {
        this.panelCode = panelCode;
    }

    public String getModuleCodeList() {
        return moduleCodeList;
    }

    public void setModuleCodeList(String moduleCodeList) {
        this.moduleCodeList = moduleCodeList;
    }
}
