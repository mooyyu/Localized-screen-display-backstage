package pojo;

public class StuSourceDistribution {
    private String stuSource;
    private int xsrs;

    public String getStuSource() {
        return stuSource;
    }

    public void setStuSource(String stuSource) {
        this.stuSource = stuSource;
    }

    public int getXsrs() {
        return xsrs;
    }

    public void setXsrs(int xsrs) {
        this.xsrs = xsrs;
    }
}
